        public NameException validateName(String name) {
            // Verificar por caracteres especiais no nome
            Pattern special = Pattern.compile("[!@#$%&*()_+=|<>?{}~-]");
            Matcher hasSpecial = special.matcher(name);

            // Verificar por nome incompleto
            String[] names =  name.split(" ");
            return (names.length <= 1 ? NameException.INCOMPLETE : (hasSpecial.find() ? NameException.INVALID : null));
        }

