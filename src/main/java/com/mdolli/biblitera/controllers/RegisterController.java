    private ChangeListener<String> cpfFieldChangeListener() {
        return new ChangeListener<String>() {
            @Override
            public void changed(ObservableValue<? extends String> observableValue, String oldString, String newString) {
                if((newString.length() > oldString.length()) && newString.length() > 14)
                    cpfField.setText(oldString);
                if((newString.length() > oldString.length()) && (newString.length() == 3 || newString.length() == 7))
                    cpfField.setText(newString += '.');
                if((newString.length() > oldString.length()) && newString.length() == 11)
                    cpfField.setText(newString += '-');
                System.out.println(newString.length() + ":" + oldString.length());
            }
        };
    }

    public void registerButtonOnActionEvent(ActionEvent e) throws SQLException, ClassNotFoundException {
        // Verificar condiÃ§Ãµes para realizar o cadastro
        if(nameField.getText().isEmpty()) {
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", "Por favor insira seu nome.");
            return;
        }

        if(emailField.getText().isEmpty()) {
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", "Por favor insira seu e-mail.");
            return;
        }

        if(cpfField.getText().isEmpty()) {
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", "Por favor insira seu CPF.");
            return;
        }

        if(passwordField.getText().isEmpty()) {
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", "Por favor insira sua senha.");
            return;
        }

        if(confirmPasswordField.getText().isEmpty()) {
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", "Por favor confirme sua senha.");
            return;
        }

        if(!confirmPasswordField.getText().equals(passwordField.getText())) {
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", "Senhas diferentes.");
            return;
        }

        Validator validator = new Validator();
        Validator.NameException nameError = validator.validateName(nameField.getText());
        Validator.EmailException emailError = validator.validateEmail(emailField.getText());
        Validator.CpfException cpfError = validator.validateCpf(cpfField.getText());
        String errorString = null;

        if(cpfError != null) {
            switch (cpfError) {
                case INVALID -> errorString = "CPF invÃ¡lido.";
                case INCOMPLETE -> errorString = "CPF incompleto.";
                default -> errorString = "Verifique o seu CPF novamente.";
            };
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", errorString);
            return;
        }

        if(nameError != null) {
            switch(nameError) {
                case INVALID -> errorString = "Nome invÃ¡lido.";
                case INCOMPLETE -> errorString = "Nome incompleto.";
                default -> errorString = "Verifique o seu nome novamente.";
            };
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", errorString);
            return;
        }

        if(emailError != null) {
            switch(emailError) {
                case INVALID -> errorString = "E-mail invÃ¡lido.";
                default -> errorString = "Verifique o seu e-mail novamente.";
            };
            Utility.createAlertBox(Alert.AlertType.ERROR, "Erro!", errorString);
            return;
        }

        AuthenticationController authController = new AuthenticationController();
        User user = authController.Register(
                emailField.getText(),
                passwordField.getText(),
                nameField.getText(),
                cpfField.getText()
        );
        if(user != null) {
            FXMLLoader pageLoader = new FXMLLoader(Application.class.getResource("home-view.fxml"));
            HomeController homeController = new HomeController();
            homeController.setAuthenticationController(authController);
            homeController.setCurrentUser(user);
            pageLoader.setController(homeController);
            authController.loadAuthenticatedPage(this.rootPane, pageLoader);
        }
    }

